import UIKit

extension UIFont {
    
    static func getSDBoldFontsWith(size: CGFloat) -> UIFont {
        return UIFont(name: "CircularStd-Bold", size: size)!
    }
    static func getSDBookFontsWith(size: CGFloat) -> UIFont {
        return UIFont(name: "CircularStd-Book", size: size)!
    }
    
}
